#include "3-calc.h"
/**
 * op_add - displays sum of two numbers.
 * @a: The first number.
 * @b: The second number.
 * Return: The sum of a and b.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  * op_sub - displays the difference between two numbers
  * @a: first number
  * @b: second number
  * Return: difference between a and b
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  * op_mul - displays the product of two numbers
  * @a: first number
  * @b: second number
  * Return: the product of a and b
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  * op_div - displays the result of division between two numbers
  * @a: first number
  * @b: second number
  * Return: the result of division betweeen a and b
  */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  * op_mod - displays remainder after division of two numbers
  * @a: first letter
  * @b: second letter
  * Return: the remainder after division of two numbers
  */
int op_mod(int a, int b)
{
	return (a % b);
}

